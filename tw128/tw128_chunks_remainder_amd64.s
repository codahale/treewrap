// SETUP_PTR_N points instance i's src/dst pointer slots at src + i*8183 (base
// AX) and dst + i*8183 (base R9) with stride 167 when i < n (CX), or at the
// in-frame dummy block (DX) with stride 0 when i >= n, so an inactive instance
// reads and writes only the dummy block and never advances. Temps R10-R12.
#define SETUP_PTR_N(i, srcslot, dstslot, strideslot) \
	LEAQ	(i*8183)(AX), R10; \
	LEAQ	(i*8183)(R9), R11; \
	MOVQ	$167, R12; \
	CMPQ	CX, $i; \
	JGT	4(PC); \
	MOVQ	DX, R10; \
	MOVQ	DX, R11; \
	XORQ	R12, R12; \
	MOVQ	R10, srcslot(SP); \
	MOVQ	R11, dstslot(SP); \
	MOVQ	R12, strideslot(SP)

// ADVANCE_PTRS_N advances the four src and four dst block pointers by their
// per-instance strides (167 for active instances, 0 for dummy-pinned ones).
#define ADVANCE_PTRS_N \
	ADDQ	1680(SP), AX; \
	ADDQ	1688(SP), CX; \
	ADDQ	1696(SP), DX; \
	ADDQ	1704(SP), BX; \
	ADDQ	1680(SP), SI; \
	ADDQ	1688(SP), BP; \
	ADDQ	1696(SP), R13; \
	ADDQ	1704(SP), R14


// ENC_LANE_GS_N / DEC_LANE_GS_N are the masked variants of ENC_LANE_GS /
// DEC_LANE_GS: they gather/scatter only the K2-selected lanes (the low n chunk
// instances) instead of all eight, so the kernel can read a 2..7 chunk remainder
// directly from the message with no padding buffer. K2 holds the persistent
// n-lane mask; gather/scatter zero their writemask (K1), so it is reloaded from
// K2 before each. Inactive lanes generate no memory access (no fault past the
// end of the remainder) and run as independent garbage instances, ignored.
#define ENC_LANE_GS_N(offset, Zlane) \
	KMOVW	K2, K1; \
	VPGATHERQQ	offset(BX)(Z28*1), K1, Z25; \
	VPXORQ	Z25, Zlane, Z26; \
	KMOVW	K2, K1; \
	VPSCATTERQQ	Z26, K1, offset(R14)(Z28*1); \
	VPXORQ	Z26, Zlane, Zlane

#define DEC_LANE_GS_N(offset, Zlane) \
	KMOVW	K2, K1; \
	VPGATHERQQ	offset(BX)(Z28*1), K1, Z25; \
	VPXORQ	Z25, Zlane, Z26; \
	KMOVW	K2, K1; \
	VPSCATTERQQ	Z26, K1, offset(R14)(Z28*1); \
	VPXORQ	Z25, Zlane, Zlane


// func encryptChunksBodyAVX512N(s *state8, src, dst *byte, n, blocks uint64)
//
// Register-resident AVX-512 kernel for a 2..7 chunk remainder: it
// gathers/scatters only the low n lanes (mask K2), reading the n chunks
// directly from src/dst with no scratch buffer. Lanes n..7 are independent
// garbage instances and are never read or written. The tail block and tags
// are completed by finishEncryptChunkLanes in Go.
//
// Gathers are the right tool below full width: a masked variant of the
// transposed kernel (per-chunk mask registers on the tile loads/stores) was
// measured slower at every n in 2..7 on Emerald Rapids — the transpose's
// shuffle work is constant regardless of n, so it only amortizes at the full
// 8-chunk width, where encryptChunksBodyAVX512T already runs.
TEXT ·encryptChunksBodyAVX512N(SB), $64-40
	MOVQ	s+0(FP), AX
	MOVQ	src+8(FP), BX
	MOVQ	dst+16(FP), R14

	MOVQ	n+24(FP), CX
	MOVQ	$1, DX
	SHLQ	CX, DX
	SUBQ	$1, DX
	KMOVW	DX, K2			// persistent low-n-lane mask

	MOVQ	$0, 0(SP)
	MOVQ	$8183, 8(SP)
	MOVQ	$16366, 16(SP)
	MOVQ	$24549, 24(SP)
	MOVQ	$32732, 32(SP)
	MOVQ	$40915, 40(SP)
	MOVQ	$49098, 48(SP)
	MOVQ	$57281, 56(SP)

	VMOVDQU64	0*64(AX), Z0
	VMOVDQU64	1*64(AX), Z1
	VMOVDQU64	2*64(AX), Z2
	VMOVDQU64	3*64(AX), Z3
	VMOVDQU64	4*64(AX), Z4
	VMOVDQU64	5*64(AX), Z5
	VMOVDQU64	6*64(AX), Z6
	VMOVDQU64	7*64(AX), Z7
	VMOVDQU64	8*64(AX), Z8
	VMOVDQU64	9*64(AX), Z9
	VMOVDQU64	10*64(AX), Z10
	VMOVDQU64	11*64(AX), Z11
	VMOVDQU64	12*64(AX), Z12
	VMOVDQU64	13*64(AX), Z13
	VMOVDQU64	14*64(AX), Z14
	VMOVDQU64	15*64(AX), Z15
	VMOVDQU64	16*64(AX), Z16
	VMOVDQU64	17*64(AX), Z17
	VMOVDQU64	18*64(AX), Z18
	VMOVDQU64	19*64(AX), Z19
	VMOVDQU64	20*64(AX), Z20
	VMOVDQU64	21*64(AX), Z21
	VMOVDQU64	22*64(AX), Z22
	VMOVDQU64	23*64(AX), Z23
	VMOVDQU64	24*64(AX), Z24

	MOVQ	$0x00FFFFFFFFFFFFFF, R13
	MOVQ	$0x9A00000000000000, DI

	MOVQ	blocks+32(FP), R12

tw128_enc_avx512n_loop:
	VMOVDQU64	0(SP), Z28

	ENC_LANE_GS_N(0*8, Z0)
	ENC_LANE_GS_N(1*8, Z1)
	ENC_LANE_GS_N(2*8, Z2)
	ENC_LANE_GS_N(3*8, Z3)
	ENC_LANE_GS_N(4*8, Z4)
	ENC_LANE_GS_N(5*8, Z5)
	ENC_LANE_GS_N(6*8, Z6)
	ENC_LANE_GS_N(7*8, Z7)
	ENC_LANE_GS_N(8*8, Z8)
	ENC_LANE_GS_N(9*8, Z9)
	ENC_LANE_GS_N(10*8, Z10)
	ENC_LANE_GS_N(11*8, Z11)
	ENC_LANE_GS_N(12*8, Z12)
	ENC_LANE_GS_N(13*8, Z13)
	ENC_LANE_GS_N(14*8, Z14)
	ENC_LANE_GS_N(15*8, Z15)
	ENC_LANE_GS_N(16*8, Z16)
	ENC_LANE_GS_N(17*8, Z17)
	ENC_LANE_GS_N(18*8, Z18)
	ENC_LANE_GS_N(19*8, Z19)

	VPBROADCASTQ	R13, Z27
	KMOVW	K2, K1
	VPGATHERQQ	160(BX)(Z28*1), K1, Z25
	VPXORQ	Z25, Z20, Z26
	VPANDQ	Z27, Z26, Z26
	// The scatter writes whole qwords, so byte 7 of the stored value must be
	// the original gathered byte (the next block's first source byte): a
	// fully-overlapping dst would otherwise be clobbered before it is read.
	VPANDNQ	Z25, Z27, Z29
	VPORQ	Z26, Z29, Z29
	KMOVW	K2, K1
	VPSCATTERQQ	Z29, K1, 160(R14)(Z28*1)
	VPXORQ	Z26, Z20, Z20

	VPBROADCASTQ	DI, Z26
	VPXORQ	Z26, Z20, Z20

	LEAQ	tw128_round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	ADDQ	$167, BX
	ADDQ	$167, R14
	SUBQ	$1, R12
	JNZ	tw128_enc_avx512n_loop

	VMOVDQU64	Z0, 0*64(AX)
	VMOVDQU64	Z1, 1*64(AX)
	VMOVDQU64	Z2, 2*64(AX)
	VMOVDQU64	Z3, 3*64(AX)
	VMOVDQU64	Z4, 4*64(AX)
	VMOVDQU64	Z5, 5*64(AX)
	VMOVDQU64	Z6, 6*64(AX)
	VMOVDQU64	Z7, 7*64(AX)
	VMOVDQU64	Z8, 8*64(AX)
	VMOVDQU64	Z9, 9*64(AX)
	VMOVDQU64	Z10, 10*64(AX)
	VMOVDQU64	Z11, 11*64(AX)
	VMOVDQU64	Z12, 12*64(AX)
	VMOVDQU64	Z13, 13*64(AX)
	VMOVDQU64	Z14, 14*64(AX)
	VMOVDQU64	Z15, 15*64(AX)
	VMOVDQU64	Z16, 16*64(AX)
	VMOVDQU64	Z17, 17*64(AX)
	VMOVDQU64	Z18, 18*64(AX)
	VMOVDQU64	Z19, 19*64(AX)
	VMOVDQU64	Z20, 20*64(AX)
	VMOVDQU64	Z21, 21*64(AX)
	VMOVDQU64	Z22, 22*64(AX)
	VMOVDQU64	Z23, 23*64(AX)
	VMOVDQU64	Z24, 24*64(AX)

	VZEROUPPER
	RET


