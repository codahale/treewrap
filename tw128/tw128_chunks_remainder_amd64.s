// TW128 remainder chunk processing — AMD64 n-wide body kernels.
//
// These kernels process full rho (167-byte) blocks of a 2..7 chunk remainder
// under the strict BDPV11 framing and store the resulting state back into
// state8. The AVX-512 kernels take the block count as a parameter (48 for a
// complete chunk body) so a ragged-tail lane can share a batch for its full
// blocks; the final full rho-block — closed with MSG_LAST — and the active
// tag extraction are completed in Go (finish{Encrypt,Decrypt}ChunkLanes).
// 8183 = 49 × 167, so a complete chunk has no ragged tail.
//
// Per full block: 20 whole lanes (160 bytes) + a 7-byte partial lane 20, then
// MSG_MORE (0x1A) | pad (0x80) = 0x9A XORed at byte 167 (lane 20, byte 7), then
// one permutation. The partial lane keeps byte 7 of lane 20 (the suffix slot)
// holding keystream by masking the absorbed ciphertext to its low 7 bytes.

//go:build !purego

#include "textflag.h"
#include "permute_amd64_avx2.h"
#include "permute_amd64_avx512.h"

// ENC_LANE_X4 encrypts lane i for 4 instances against the x4 state buffer at R8.
// ct = pt ^ ks, store ct, buffer ^= ct (= pt). Clobbers R10.
// AX=src0, CX=src1, DX=src2, BX=src3, SI=dst0, BP=dst1, R13=dst2, R14=dst3.
#define ENC_LANE_X4(i) \
	MOVQ i*8(AX), R10; XORQ i*32+0(R8), R10;  MOVQ R10, i*8(SI);  XORQ R10, i*32+0(R8);  \
	MOVQ i*8(CX), R10; XORQ i*32+8(R8), R10;  MOVQ R10, i*8(BP);  XORQ R10, i*32+8(R8);  \
	MOVQ i*8(DX), R10; XORQ i*32+16(R8), R10; MOVQ R10, i*8(R13); XORQ R10, i*32+16(R8); \
	MOVQ i*8(BX), R10; XORQ i*32+24(R8), R10; MOVQ R10, i*8(R14); XORQ R10, i*32+24(R8)

// DEC_LANE_X4 decrypts lane i for 4 instances: pt = ct ^ ks, store pt, buffer
// ^= ct (= pt). Clobbers R10, R11.
#define DEC_LANE_X4(i) \
	MOVQ i*8(AX), R10; MOVQ i*32+0(R8), R11;  XORQ R10, R11; MOVQ R11, i*8(SI);  XORQ R10, i*32+0(R8);  \
	MOVQ i*8(CX), R10; MOVQ i*32+8(R8), R11;  XORQ R10, R11; MOVQ R11, i*8(BP);  XORQ R10, i*32+8(R8);  \
	MOVQ i*8(DX), R10; MOVQ i*32+16(R8), R11; XORQ R10, R11; MOVQ R11, i*8(R13); XORQ R10, i*32+16(R8); \
	MOVQ i*8(BX), R10; MOVQ i*32+24(R8), R11; XORQ R10, R11; MOVQ R11, i*8(R14); XORQ R10, i*32+24(R8)

// ENC_PARTIAL7_X4 encrypts the 7-byte partial lane 20 (offset 160) for 4
// instances. Reads 8 bytes (the 8th lies within the chunk for every full
// block), stores exactly 7 bytes, and XORs the low-7-byte ciphertext into the
// buffer so byte 7 of lane 20 keeps its keystream. Clobbers R10, R11.
#define ENC_PARTIAL7_X4 \
	MOVQ 160(AX), R10; XORQ 640(R8), R10; MOVL R10, 160(SI);  MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(SI);  SHRQ $16, R11; MOVB R11, 166(SI);  MOVQ R10, R11; SHLQ $8, R11; SHRQ $8, R11; XORQ R11, 640(R8); \
	MOVQ 160(CX), R10; XORQ 648(R8), R10; MOVL R10, 160(BP);  MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(BP);  SHRQ $16, R11; MOVB R11, 166(BP);  MOVQ R10, R11; SHLQ $8, R11; SHRQ $8, R11; XORQ R11, 648(R8); \
	MOVQ 160(DX), R10; XORQ 656(R8), R10; MOVL R10, 160(R13); MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(R13); SHRQ $16, R11; MOVB R11, 166(R13); MOVQ R10, R11; SHLQ $8, R11; SHRQ $8, R11; XORQ R11, 656(R8); \
	MOVQ 160(BX), R10; XORQ 664(R8), R10; MOVL R10, 160(R14); MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(R14); SHRQ $16, R11; MOVB R11, 166(R14); MOVQ R10, R11; SHLQ $8, R11; SHRQ $8, R11; XORQ R11, 664(R8)

// DEC_PARTIAL7_X4 decrypts the 7-byte partial lane 20 for 4 instances. Clobbers
// R10, R11, R12.
#define DEC_PARTIAL7_X4 \
	MOVQ 160(AX), R10; MOVQ R10, R12; XORQ 640(R8), R10; MOVL R10, 160(SI);  MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(SI);  SHRQ $16, R11; MOVB R11, 166(SI);  SHLQ $8, R12; SHRQ $8, R12; XORQ R12, 640(R8); \
	MOVQ 160(CX), R10; MOVQ R10, R12; XORQ 648(R8), R10; MOVL R10, 160(BP);  MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(BP);  SHRQ $16, R11; MOVB R11, 166(BP);  SHLQ $8, R12; SHRQ $8, R12; XORQ R12, 648(R8); \
	MOVQ 160(DX), R10; MOVQ R10, R12; XORQ 656(R8), R10; MOVL R10, 160(R13); MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(R13); SHRQ $16, R11; MOVB R11, 166(R13); SHLQ $8, R12; SHRQ $8, R12; XORQ R12, 656(R8); \
	MOVQ 160(BX), R10; MOVQ R10, R12; XORQ 664(R8), R10; MOVL R10, 160(R14); MOVQ R10, R11; SHRQ $32, R11; MOVW R11, 164(R14); SHRQ $16, R11; MOVB R11, 166(R14); SHLQ $8, R12; SHRQ $8, R12; XORQ R12, 664(R8)

// MSG_MORE_X4 XORs (MSG_MORE 0x1A | pad 0x80) = 0x9A at byte 167 (lane 20, byte
// 7) of the x4 state buffer for 4 instances. Clobbers R10.
#define MSG_MORE_X4 \
	MOVQ $0x9A00000000000000, R10; \
	XORQ R10, 640(R8); \
	XORQ R10, 648(R8); \
	XORQ R10, 656(R8); \
	XORQ R10, 664(R8)

