// TW128 chunk processing — AVX-512 fused and AVX2 body-only implementations.
//
// The AVX-512 entrypoints process 8 × 8128-byte chunks end-to-end.
// The AVX2 entrypoints process the 48 full 168-byte stripes of 8 × 8128-byte
// chunks and leave the 64-byte tail/tag finish to Go.

//go:build !purego

#include "textflag.h"
