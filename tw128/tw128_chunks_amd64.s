	DECRYPT_LANE_X8_GS(16*8, Z16)
	DECRYPT_LANE_X8_GS(17*8, Z17)
	DECRYPT_LANE_X8_GS(18*8, Z18)
	DECRYPT_LANE_X8_GS(19*8, Z19)
	DECRYPT_LANE_X8_GS(20*8, Z20)

	// Capacity XOR: 0x01 into lane 21.
	MOVQ	$1, AX
	VPBROADCASTQ	AX, Z25
	VPXORQ	Z25, Z21, Z21

	LEAQ	tw128_round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	ADDQ	$168, BX
	ADDQ	$168, R14
	SUBQ	$1, R12
	JNZ	tw128_dec_avx512_loop

	VMOVDQU64	0(SP), Z28
	DECRYPT_LANE_X8_GS(0*8, Z0)
	DECRYPT_LANE_X8_GS(1*8, Z1)
	DECRYPT_LANE_X8_GS(2*8, Z2)
	DECRYPT_LANE_X8_GS(3*8, Z3)
	DECRYPT_LANE_X8_GS(4*8, Z4)
	DECRYPT_LANE_X8_GS(5*8, Z5)
	DECRYPT_LANE_X8_GS(6*8, Z6)
	DECRYPT_LANE_X8_GS(7*8, Z7)

	MOVQ	$1, AX
	VPBROADCASTQ	AX, Z25
	VPXORQ	Z25, Z8, Z8
	VPXORQ	Z25, Z21, Z21

	LEAQ	tw128_round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	MOVQ	$0, 0(SP)
	MOVQ	$32, 8(SP)
	MOVQ	$64, 16(SP)
	MOVQ	$96, 24(SP)
	MOVQ	$128, 32(SP)
	MOVQ	$160, 40(SP)
	MOVQ	$192, 48(SP)
	MOVQ	$224, 56(SP)
	VMOVDQU64	0(SP), Z28

	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z0, K1, 0(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z1, K1, 8(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z2, K1, 16(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z3, K1, 24(DI)(Z28*1)

	VZEROUPPER
	RET


// func encryptChunksBodyAVX2(s *state8, src, dst *byte)
//
// 2× x4 approach (half 0: instances 0-3, half 1: instances 4-7).
//
// Frame: 1688 bytes local (0-799 = buffer A, 800-1599 = buffer B,
//   1600-1631 = 4 src ptrs, 1632-1663 = 4 dst ptrs,
//   1664 = count, 1672 = saved state ptr), 24 bytes args.
TEXT ·encryptChunksBodyAVX2(SB), $1688-24
	MOVQ	s+0(FP), DI
	MOVQ	src+8(FP), AX
	MOVQ	dst+16(FP), R9
	MOVQ	DI, 1672(SP)		// save state ptr

	// === Half 0: instances 0-3 ===
	// Src pointers: src + {0, 1, 2, 3}×8128.
	MOVQ	AX, 1600(SP)
	LEAQ	8128(AX), R10
	MOVQ	R10, 1608(SP)
	LEAQ	8128(R10), R10
	MOVQ	R10, 1616(SP)
	LEAQ	8128(R10), R10
	MOVQ	R10, 1624(SP)
	// Dst pointers: dst + {0, 1, 2, 3}×8128.
	MOVQ	R9, 1632(SP)
	LEAQ	8128(R9), R10
	MOVQ	R10, 1640(SP)
	LEAQ	8128(R10), R10
	MOVQ	R10, 1648(SP)
	LEAQ	8128(R10), R10
	MOVQ	R10, 1656(SP)
	MOVQ	$48, 1664(SP)		// stripe count

	// Load instances 0-3 from State8.
	VMOVDQU	0*64(DI), Y0;   VMOVDQU	Y0, 0*32(SP)
	VMOVDQU	1*64(DI), Y0;   VMOVDQU	Y0, 1*32(SP)
	VMOVDQU	2*64(DI), Y0;   VMOVDQU	Y0, 2*32(SP)
	VMOVDQU	3*64(DI), Y0;   VMOVDQU	Y0, 3*32(SP)
	VMOVDQU	4*64(DI), Y0;   VMOVDQU	Y0, 4*32(SP)
	VMOVDQU	5*64(DI), Y0;   VMOVDQU	Y0, 5*32(SP)
	VMOVDQU	6*64(DI), Y0;   VMOVDQU	Y0, 6*32(SP)
	VMOVDQU	7*64(DI), Y0;   VMOVDQU	Y0, 7*32(SP)
	VMOVDQU	8*64(DI), Y0;   VMOVDQU	Y0, 8*32(SP)
	VMOVDQU	9*64(DI), Y0;   VMOVDQU	Y0, 9*32(SP)
	VMOVDQU	10*64(DI), Y0;  VMOVDQU	Y0, 10*32(SP)
	VMOVDQU	11*64(DI), Y0;  VMOVDQU	Y0, 11*32(SP)
	VMOVDQU	12*64(DI), Y0;  VMOVDQU	Y0, 12*32(SP)
	VMOVDQU	13*64(DI), Y0;  VMOVDQU	Y0, 13*32(SP)
	VMOVDQU	14*64(DI), Y0;  VMOVDQU	Y0, 14*32(SP)
	VMOVDQU	15*64(DI), Y0;  VMOVDQU	Y0, 15*32(SP)
	VMOVDQU	16*64(DI), Y0;  VMOVDQU	Y0, 16*32(SP)
	VMOVDQU	17*64(DI), Y0;  VMOVDQU	Y0, 17*32(SP)
	VMOVDQU	18*64(DI), Y0;  VMOVDQU	Y0, 18*32(SP)
