// func encryptChunksAVX512(s *state8, src, dst *byte, tags *byte)
//
// Processes 8 × 8128-byte chunks end-to-end, including the 64-byte tail,
// final body padding, and tag extraction.
//
// TW128 chunk constants (hardcoded):
//   Rate = 168, BlockSize = 8128 = 48 × 168 + 64
//
// Frame: 64 bytes local (gather indices), 32 bytes args.
TEXT ·encryptChunksAVX512(SB), $64-32
	MOVQ	s+0(FP), AX
	MOVQ	src+8(FP), BX
	MOVQ	dst+16(FP), R14
	MOVQ	tags+24(FP), DI

	// Build gather/scatter index vector {0, 8128, 2×8128, ..., 7×8128} at SP+0.
	MOVQ	$0, 0(SP)
	MOVQ	$8128, 8(SP)
	MOVQ	$16256, 16(SP)
	MOVQ	$24384, 24(SP)
	MOVQ	$32512, 32(SP)
	MOVQ	$40640, 40(SP)
	MOVQ	$48768, 48(SP)
	MOVQ	$56896, 56(SP)

	// Load state from State8 into Z0-Z24.
	VMOVDQU64	0*64(AX), Z0
	VMOVDQU64	1*64(AX), Z1
	VMOVDQU64	2*64(AX), Z2
	VMOVDQU64	3*64(AX), Z3
	VMOVDQU64	4*64(AX), Z4
	VMOVDQU64	5*64(AX), Z5
	VMOVDQU64	6*64(AX), Z6
	VMOVDQU64	7*64(AX), Z7
	VMOVDQU64	8*64(AX), Z8
	VMOVDQU64	9*64(AX), Z9
	VMOVDQU64	10*64(AX), Z10
	VMOVDQU64	11*64(AX), Z11
	VMOVDQU64	12*64(AX), Z12
	VMOVDQU64	13*64(AX), Z13
	VMOVDQU64	14*64(AX), Z14
	VMOVDQU64	15*64(AX), Z15
	VMOVDQU64	16*64(AX), Z16
	VMOVDQU64	17*64(AX), Z17
	VMOVDQU64	18*64(AX), Z18
	VMOVDQU64	19*64(AX), Z19
	VMOVDQU64	20*64(AX), Z20
	VMOVDQU64	21*64(AX), Z21
	VMOVDQU64	22*64(AX), Z22
	VMOVDQU64	23*64(AX), Z23
	VMOVDQU64	24*64(AX), Z24

	// Loop 48 full stripes.
	MOVQ	$48, R12

tw128_enc_avx512_loop:
	VMOVDQU64	0(SP), Z28

	ENCRYPT_LANE_X8_GS(0*8, Z0)
	ENCRYPT_LANE_X8_GS(1*8, Z1)
	ENCRYPT_LANE_X8_GS(2*8, Z2)
	ENCRYPT_LANE_X8_GS(3*8, Z3)
	ENCRYPT_LANE_X8_GS(4*8, Z4)
	ENCRYPT_LANE_X8_GS(5*8, Z5)
	ENCRYPT_LANE_X8_GS(6*8, Z6)
	ENCRYPT_LANE_X8_GS(7*8, Z7)
	ENCRYPT_LANE_X8_GS(8*8, Z8)
	ENCRYPT_LANE_X8_GS(9*8, Z9)
	ENCRYPT_LANE_X8_GS(10*8, Z10)
	ENCRYPT_LANE_X8_GS(11*8, Z11)
	ENCRYPT_LANE_X8_GS(12*8, Z12)
	ENCRYPT_LANE_X8_GS(13*8, Z13)
	ENCRYPT_LANE_X8_GS(14*8, Z14)
	ENCRYPT_LANE_X8_GS(15*8, Z15)
	ENCRYPT_LANE_X8_GS(16*8, Z16)
	ENCRYPT_LANE_X8_GS(17*8, Z17)
	ENCRYPT_LANE_X8_GS(18*8, Z18)
	ENCRYPT_LANE_X8_GS(19*8, Z19)
	ENCRYPT_LANE_X8_GS(20*8, Z20)

	// Capacity XOR: 0x01 into lane 21.
	MOVQ	$1, AX
	VPBROADCASTQ	AX, Z25
	VPXORQ	Z25, Z21, Z21

	LEAQ	tw128_round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	ADDQ	$168, BX
	ADDQ	$168, R14
	SUBQ	$1, R12
	JNZ	tw128_enc_avx512_loop

	// Tail block: 64 bytes = 8 full lanes at the current aligned offset.
	VMOVDQU64	0(SP), Z28
	ENCRYPT_LANE_X8_GS(0*8, Z0)
	ENCRYPT_LANE_X8_GS(1*8, Z1)
	ENCRYPT_LANE_X8_GS(2*8, Z2)
	ENCRYPT_LANE_X8_GS(3*8, Z3)
	ENCRYPT_LANE_X8_GS(4*8, Z4)
	ENCRYPT_LANE_X8_GS(5*8, Z5)
	ENCRYPT_LANE_X8_GS(6*8, Z6)
	ENCRYPT_LANE_X8_GS(7*8, Z7)

	// bodyPadStarPermute at pos=64: pad into lane 8 and capacity into lane 21.
	MOVQ	$1, AX
	VPBROADCASTQ	AX, Z25
	VPXORQ	Z25, Z8, Z8
	VPXORQ	Z25, Z21, Z21

	LEAQ	tw128_round_consts_2x+192(SB), R11
	X8_4ROUNDS_AVX512(0, 16, 32, 48)
	X8_4ROUNDS_AVX512(64, 80, 96, 112)
	X8_4ROUNDS_AVX512(128, 144, 160, 176)

	// Extract tags via VPSCATTERQQ.
	MOVQ	$0, 0(SP)
	MOVQ	$32, 8(SP)
	MOVQ	$64, 16(SP)
	MOVQ	$96, 24(SP)
	MOVQ	$128, 32(SP)
	MOVQ	$160, 40(SP)
	MOVQ	$192, 48(SP)
	MOVQ	$224, 56(SP)
	VMOVDQU64	0(SP), Z28

	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z0, K1, 0(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z1, K1, 8(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z2, K1, 16(DI)(Z28*1)
	KXNORB	K1, K1, K1
	VPSCATTERQQ	Z3, K1, 24(DI)(Z28*1)

	VZEROUPPER
	RET


// func decryptChunksAVX512(s *state8, src, dst *byte, tags *byte)
