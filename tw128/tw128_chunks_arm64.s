// DEC_PARTIAL1 decrypts the final 1-byte partial lane 1 (tail byte 8).
#define DEC_PARTIAL1 \
	VMOV V1.D[0], R9; MOVBU (R2), R10; EOR R9, R10, R11; AND $0xFF, R10, R12; EOR R12, R9, R9; VMOV R9, V1.D[0]; MOVB R11, (R5); ADD $1, R2; ADD $1, R5; \
