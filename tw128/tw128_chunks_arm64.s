	VDUP	V1.D[1], V25.D2; VST1 [V25.D1], (R6); ADD $8, R6; \
	VDUP	V2.D[1], V25.D2; VST1 [V25.D1], (R6); ADD $8, R6; \
	VDUP	V3.D[1], V25.D2; VST1 [V25.D1], (R6); ADD $8, R6


