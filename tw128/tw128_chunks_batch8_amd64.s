	LEAQ	8183(R10), R10
	MOVQ	R10, 1616(SP)
	LEAQ	8183(R10), R10
	MOVQ	R10, 1624(SP)
	MOVQ	R9, 1632(SP)
	LEAQ	8183(R9), R10
	MOVQ	R10, 1640(SP)
	LEAQ	8183(R10), R10
	MOVQ	R10, 1648(SP)
